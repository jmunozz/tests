#include "libft.h"

int		ft_size_tab(char **tab)
{
	char	**tmp;
	int		i;

	i = 0;
	tmp = tab;
	if (!tab || !*tab)
		return (i);
	while (*tmp)
	{
		i++;
		tmp++;
	}
	return (i + 1);
}
